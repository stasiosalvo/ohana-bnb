export function roomDescription(
  room: (typeof GALLERIA_ROOMS)[number],
  lang: SiteLang
): string {
  switch (lang) {
    case "en":
      return room.descriptionEn;
    case "fr":
      return room.descriptionFr;
    case "es":
      return room.descriptionEs;
    default:
      return room.descriptionIt;
  }
}

export function roomMeta(
  room: (typeof GALLERIA_ROOMS)[number],
  lang: SiteLang
): string {
  switch (lang) {
    case "en":
      return room.metaEn;
    case "fr":
      return room.metaFr;
    case "es":
      return room.metaEs;
    default:
      return room.metaIt;
  }
}
